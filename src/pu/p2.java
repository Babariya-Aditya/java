package pu;

public class p2 {
    public static void main (String[] args){
        //bitwise operation
        int a=4;
        int b=3;
        System.out.println("a | b =" +(a|b));
        System.out.println("a & b =" +(a & b));
        System.out.println("a ^ b =" +(a ^ b));
        System.out.println(" complement of a =" +(~a));
        System.out.println(" complement of b =" +(~b));


    }
}
